#Realiza o ordinograma correspondente a un programa que saque por pantalla a porcentaxe descontada nunha compra.
# Introducindo, por teclado, o prezo da tarifa e o prezo pagado.
#
#Inicio
#  ↓
#[ Ler prezo_tarifa ]
#  ↓
#[ Ler prezo_pagado ]
#  ↓
#[ desconto ← prezo_tarifa - prezo_pagado ]
#  ↓
#[ porcentaxe ← (desconto / prezo_tarifa) * 100 ]
#  ↓
#[ Amosar porcentaxe descontada ]
#  ↓
#Fin